'''
Logical Operators - Logical operators are used to combine conditional statements. Some are given below.
==, !=, >, <, >=, <=, and, or, not
'''

print("Hello" == "Hello")
print(7 != 7)
print(5 > 3)
print(20 < 10)
print(100 >= 100)
print(200 <= 100)
print('a' > 'A')  # True because of ASCII value. As ASCII value of A is 65 and a is 97.
print(1 < 2 > 3 < 4 < 5)
print(1 < 2 < 3 < 4 < 5)
print(not (True))
print(not (1 != 1))

